export function createLayer({ id, title, tabs }) {
  const layer = document.createElement('div');
  layer.id = id;

  let activeTabId = tabs[0]?.id;

  layer.innerHTML = `
    <div class="panel">
      <div class="header">
        <div class="header-top">
          <div class="brand">${SVC_LOGO_SVG}</div>

          <button type="button" class="close-button" aria-label="닫기">
            <span class="material-symbols-rounded" aria-hidden="true">close</span>
          </button>
        </div>

        <div class="page-url">
          <span class="material-symbols-rounded link-icon" aria-hidden="true">link</span>
          <span>${window.location.href}</span>
        </div>
      </div>

      <div class="tabs"></div>
      <div class="results"></div>
    </div>
  `;

  document.body.appendChild(layer);

  const tabsContainer = layer.querySelector('.tabs');
  const resultsContainer = layer.querySelector('.results');
  const closeButton = layer.querySelector('.close-button');

  function closeLayer() {
    document.removeEventListener('pointerdown', handleOutsideClick, true);
    layer.remove();
  }

  function handleOutsideClick(event) {
    const path = event.composedPath?.() || [];
    const isInsideLayer = path.includes(layer) || layer.contains(event.target);

    if (!isInsideLayer) {
      closeLayer();
    }
  }

  function render() {
    const activeTab = tabs.find((tab) => tab.id === activeTabId);

    renderTabs({
      container: tabsContainer,
      tabs,
      activeTabId,
      onChange: (tabId) => {
        activeTabId = tabId;
        render();
      },
    });

    renderResults({
      container: resultsContainer,
      tab: activeTab,
    });
  }

  closeButton.addEventListener('click', () => {
    closeLayer();
  });

  render();
  
  setTimeout(() => {
    document.addEventListener('pointerdown', handleOutsideClick, true);
  }, 0);

}